// 示例 1：
// 输入：[1,2,3,1]
// 输出：4
// 解释：偷窃 1 号房屋 (金额 = 1) ，然后偷窃 3 号房屋 (金额 = 3)。
//      偷窃到的最高金额 = 1 + 3 = 4 。
// 示例 2：
// 输入：[2,7,9,3,1]
// 输出：12
// 解释：偷窃 1 号房屋 (金额 = 2), 偷窃 3 号房屋 (金额 = 9)，接着偷窃 5 号房屋 (金额 = 1)。
//      偷窃到的最高金额 = 2 + 9 + 1 = 12 。
function getNum(arr) {
    let a = 0;
    let b = 0;
    for (let i = 0; i < arr.length; i++) {
        if(i%2 === 0) {
            a+=arr[i]
        }else{
            b+=arr[i]
        }
    }
    return a>b?a:b;
}

console.log(getNum([2,7,9,3,1]))
